for (const article of articles) {
    article.imageUrl = articleImages[article.title];
}

async function main() {
    const titles = articles.map((article) => article.title);

    const existingArticles = await prisma.article.findMany({
        where: {
            title: {
                in: titles,
            },
        },
        select: {
            title: true,
        },
    });

    const existingTitles = new Set(
        existingArticles.map((article) => article.title)
    );
    const articlesToCreate = articles.filter(
        (article) => !existingTitles.has(article.title)
    );

    const updates = articles.map((article) =>
        prisma.article.updateMany({
            where: {
                title: article.title,
                OR: [
                    { imageUrl: null },
                    {
                        imageUrl: {
                            not: article.imageUrl,
                        },
                    },
                ],
            },
            data: {
                imageUrl: article.imageUrl,
            },
        })
    );

    const operations = [
        ...(articlesToCreate.length > 0
            ? [
                  prisma.article.createMany({
                      data: articlesToCreate,
                  }),
              ]
            : []),
        ...updates,
    ];

    const results = await prisma.$transaction(operations);
    const createCount =
        articlesToCreate.length > 0 ? results[0].count : 0;
    const updateResults =
        articlesToCreate.length > 0 ? results.slice(1) : results;

    const updatedCount = updateResults.reduce(
        (sum, result) => sum + result.count,
        0
    );

    console.log(
        `Created ${createCount} articles. Updated ${updatedCount} article images.`
    );
}

main()
    .catch((error) => {
        console.error(error);
        process.exitCode = 1;
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
